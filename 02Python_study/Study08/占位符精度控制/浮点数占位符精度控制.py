# 2浮点数占位符精度控制
name = '张三'
gender = '男'
weight = 64.425
age = 18

# %m.n
# 先来理解n，n表示我想要保留多少位小数（默认保留6位）
#   当我们的数字有3位小数
#       我们设置的n=2(小于我们浮点数小数位数)的时候会四舍五入，保留两位小数
#       我们设置的n=5(大于我们浮点数小数位数)的时候会在小数后面添0
print("我的体重是%f-----.原版" %  weight)

print("我的体重是%.2f-----.2" %  weight)
print("我的体重是%.5f-----.5" %  weight)

'''
python浮点数的精度控制使用的是银行家舍入规则
银行家舍入规则是：
当要舍入的数字小于5时，向下舍入
当要舍入的数字大于5时，向上进位
当要舍入的数字恰好是5时，向最近的偶数方向舍入（即看前面一位数字，如果是偶数则舍去5，如果是奇数则进位）'''


