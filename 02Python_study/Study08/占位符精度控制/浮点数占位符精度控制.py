# m的理解和字符串占位符一样,m表示引入字符串的最小占位长度 浮点数的字符串占位数量是（整数有几位+小数点一位+小数有几位）
# 比如说 34.553 这个小数占6位

# %m.n结合 也是先处理n再处理m

print("我的体重是%6.2f------6.2" %  weight)
